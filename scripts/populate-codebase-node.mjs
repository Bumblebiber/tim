#!/usr/bin/env node
// populate-codebase-node.mjs — MCP helper for populate-codebase-node.sh
// Reads JSON from stdin: { section, title, content, tags }
// Calls tim_write on P0063/Codebase with the correct parent

import { spawn } from 'child_process';
import { readFileSync } from 'fs';

// ---- Read input ----
const input = JSON.parse(readFileSync(0, 'utf-8'));
const { section, title, content, tags = ['#codebase', `#${section}`] } = input;

if (!section || !title) {
  console.error('Usage: echo \'{"section":"...","title":"...","content":"..."}\' | populate-codebase-node.mjs');
  process.exit(1);
}

const TIM_DB = process.env.TIM_DB_PATH || '/home/bbbee/.tim/tim.db';
const MCP_SERVER = '/home/bbbee/projects/tim/packages/tim-mcp/dist/server.js';

// ---- Call MCP ----
function mcpCall(tool, args) {
  return new Promise((resolve, reject) => {
    const s = spawn('node', [MCP_SERVER], {
      stdio: ['pipe', 'pipe', 'pipe'],
      env: { ...process.env, TIM_DB_PATH: TIM_DB }
    });

    let buf = '';
    s.stdout.on('data', d => { buf += d.toString(); });

    const req = JSON.stringify({
      jsonrpc: '2.0', id: 1,
      method: 'tools/call',
      params: { name: tool, arguments: args }
    });

    s.stdin.write(req + '\n');
    s.stdin.end();

    const timer = setTimeout(() => {
      s.kill();
      reject(new Error('MCP timeout'));
    }, 10000);

    s.on('close', () => {
      clearTimeout(timer);
      try {
        // Strip trailing non-JSON (TIMEOUT text, etc.)
        const clean = buf.substring(0, buf.lastIndexOf('}') + 1);
        const lines = clean.split('\n').filter(l => l.trim());
        for (const line of lines) {
          const obj = JSON.parse(line);
          if (obj.id === 1) {
            if (obj.error) reject(new Error(JSON.stringify(obj.error)));
            else resolve(obj);
            return;
          }
        }
        reject(new Error('No response in output'));
      } catch (e) {
        reject(new Error(`Parse error: ${e.message}\nOutput: ${buf.slice(0, 300)}`));
      }
    });
  });
}

// ---- Find or create codebase entry ----
async function main() {
  try {
    // 1. Find P0063 Codebase section
    const project = await mcpCall('tim_read', { id: 'P0063', depth: 2 });
    const codebaseNode = project.entry.children.find(c => c.title === 'Codebase — Workspace-Struktur');

    if (!codebaseNode) {
      console.error('ERROR: P0063/Codebase section not found');
      process.exit(1);
    }

    const codebaseId = codebaseNode.id;
    console.error(`[populate-codebase] Codebase section: ${codebaseId}`);

    // 2. Find existing sub-node for this section
    const codebaseData = await mcpCall('tim_read', { id: codebaseId, depth: 2 });
    const existingNode = (codebaseData.entry.children || []).find(
      c => c.metadata && c.metadata.codebaseSection === section
    );

    const body = `## ${title}\n\n${content}\n\n_Auto-generated by populate-codebase-node.sh at ${new Date().toISOString()}_`;

    if (existingNode) {
      // Update existing node
      const result = await mcpCall('tim_update', {
        id: existingNode.id,
        content: body,
        tags: tags,
        metadata: { codebaseSection: section }
      });
      const data = result.result?.content?.[0]?.text || '';
      console.error(`[populate-codebase] Updated: ${existingNode.title} → ${section}`);
      console.log(JSON.stringify({ action: 'updated', id: existingNode.id, section }));
    } else {
      // Create new node under Codebase
      const contentWithTitle = `${title}\n\n${body}`;
      const result = await mcpCall('tim_write', {
        parentId: codebaseId,
        content: contentWithTitle,
        tags: tags,
        metadata: {
          codebaseSection: section,
          type: 'standard',
          order: (codebaseData.entry.children || []).length
        }
      });
      const data = result.result?.content?.[0]?.text || '';
      console.error(`[populate-codebase] Created: ${title} under Codebase`);
      console.log(JSON.stringify({ action: 'created', section }));
    }
  } catch (err) {
    console.error(`[populate-codebase] ERROR: ${err.message}`);
    process.exit(1);
  }
}

main();
